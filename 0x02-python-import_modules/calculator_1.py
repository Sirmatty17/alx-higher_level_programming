#!/usr/bin/python3
def add(a, b):
    """My addition function
    Arg:
    a: first integer
    b: second integer
    return: value of a + b
    """
    return (a + b)

def sub(a, b):
    """My subtraction function
    Arg:
    a: first integer
    b: second integer
    Return: value of a - b
    """
    return (a - b)

def mul(a, b):
    """My Multiplication Function
     Arg:
     a: First integer
     b: Second integer
     Return: value of a * b
     """
    return (a * b)

def div(a, b):
    """My Division Function
    Arg:
    a: First integer
    b: Second integer
    Return: value of a / b
    """
    return int(a / b)
